#!/usr/bin/env node
// scripts/sync-datalake.mjs
//
// Pulls `courses.csv` and `course_career_pathways.csv` from the Workhorse
// upstream lake (`gdrive5tb:workhorse-datalake/latest/`, published by
// `RJK134/Macbook`) and writes two generated JS modules the React app
// consumes alongside its hand-curated 119-course catalogue:
//
//   frontend/src/data/datalake-courses.generated.js
//   frontend/src/data/datalake-career-pathways.generated.js
//
// METADATA / PUBLIC-COURSE-METADATA ROWS ONLY. The importer refuses any
// row whose columns include body / question / mark-scheme / answer /
// solution / applicant-PII fields. Mirrors the rule the Maieus consumer
// (`RJK134/Maieus2` PR #99) and the Shakespeare consumer
// (`RJK134/Shakespeare-is-Boring` PR #6) enforce.
//
// Usage:
//   npm run sync-datalake                       # rclone from gdrive
//   npm run sync-datalake -- --dry-run          # validate only, no write
//   npm run sync-datalake -- --source-dir <DIR> # local CSVs, no rclone

import { mkdtempSync, readFileSync, existsSync, writeFileSync, mkdirSync } from 'node:fs';
import { tmpdir } from 'node:os';
import { join, dirname, isAbsolute, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';
import { spawnSync } from 'node:child_process';

import {
  detectForbiddenColumns,
  parseCsv,
  rowsToObjects,
  validateCareerPathwayRow,
  validateCourseRow,
  validateCoursePulseRow,
  validateFundingRow,
  validateFxRow,
} from './datalake/mcm-importer.mjs';

const RCLONE_REMOTE_DIR = 'gdrive5tb:workhorse-datalake/latest';
const COURSES_CSV = 'courses.csv';
const PATHWAYS_CSV = 'course_career_pathways.csv';
const INSIGHTS_CSV = 'coursepulse_insights.csv';
const FUNDING_CSV = 'funding_opportunities.csv';
const FX_CSV = 'exchange_rates.csv';

const REPO_ROOT = resolve(dirname(fileURLToPath(import.meta.url)), '..');
const DATA_DIR = join(REPO_ROOT, 'frontend', 'src', 'data');
const API_DATA_DIR = join(REPO_ROOT, 'api', 'data');

function parseArgs(argv) {
  const args = { dryRun: false, sourceDir: undefined };
  for (let i = 0; i < argv.length; i += 1) {
    const a = argv[i];
    if (a === '--dry-run') args.dryRun = true;
    else if (a === '--source-dir') {
      args.sourceDir = argv[i + 1];
      i += 1;
    } else if (a === '--help' || a === '-h') {
      console.log(
        'Usage: npm run sync-datalake [-- --dry-run] [-- --source-dir <dir-with-CSVs>]',
      );
      process.exit(0);
    } else {
      console.error(`[sync-datalake] unknown arg: ${a}`);
      process.exit(2);
    }
  }
  return args;
}

function fetchFromRclone(stagingDir, filename) {
  const remote = `${RCLONE_REMOTE_DIR}/${filename}`;
  const dest = join(stagingDir, filename);
  const r = spawnSync('rclone', ['copyto', remote, dest, '--quiet'], { stdio: 'inherit' });
  if (r.status !== 0) {
    throw new Error(
      `rclone copyto ${remote} failed (exit ${r.status}). Ensure the gdrive5tb remote is configured.`,
    );
  }
  return dest;
}

function ingestCsv(csvPath, validator, label) {
  const text = readFileSync(csvPath, 'utf8');
  const { header, objects } = rowsToObjects(parseCsv(text));
  const forbidden = detectForbiddenColumns(header);
  if (forbidden.length > 0) {
    throw new Error(
      `[sync-datalake] REFUSING ${label} — CSV header contains forbidden columns: ${forbidden.join(', ')}`,
    );
  }
  let dropped = 0;
  const accepted = [];
  for (const obj of objects) {
    const r = validator(obj);
    if (!r.ok) {
      dropped += 1;
      continue;
    }
    accepted.push(r.row);
  }
  console.log(
    `[sync-datalake] ${label}: parsed ${objects.length} rows — accepted ${accepted.length}, dropped ${dropped}`,
  );
  return { accepted, dropped, read: objects.length };
}

function writeGeneratedFile(filename, exportName, items, sourceLabel) {
  const banner =
    `// AUTO-GENERATED by scripts/sync-datalake.mjs from the Workhorse\n` +
    `// upstream lake at gdrive5tb:workhorse-datalake/latest/.\n` +
    `// Run \`npm run sync-datalake\` to refresh. Do not hand-edit.\n` +
    `// Source: ${sourceLabel}\n` +
    `// Last sync: ${new Date().toISOString()} (${items.length} rows).\n`;
  const body = `\nexport const ${exportName} = ${JSON.stringify(items, null, 2)};\n`;
  const outPath = join(DATA_DIR, filename);
  writeFileSync(outPath, banner + body, 'utf8');
  return outPath;
}

function main() {
  const args = parseArgs(process.argv.slice(2));

  // Each entry: [filename, validator, jsExport, jsFilename, apiFilename, optional?]
  const sources = [
    [COURSES_CSV, validateCourseRow, 'lakeCourses', 'datalake-courses.generated.js', 'lake-courses.json', false],
    [PATHWAYS_CSV, validateCareerPathwayRow, 'lakeCareerPathways', 'datalake-career-pathways.generated.js', 'career-pathways.json', false],
    [INSIGHTS_CSV, validateCoursePulseRow, 'lakeInsights', 'datalake-insights.generated.js', 'insights.json', true],
    [FUNDING_CSV, validateFundingRow, 'lakeFunding', 'datalake-funding.generated.js', 'funding.json', true],
    [FX_CSV, validateFxRow, 'lakeExchangeRates', 'datalake-fx.generated.js', 'fx.json', true],
  ];

  let dir;
  let snapshotLabel;
  let cleanupStaging = false;
  if (args.sourceDir) {
    dir = isAbsolute(args.sourceDir) ? args.sourceDir : resolve(args.sourceDir);
    if (!existsSync(dir)) {
      console.error(`[sync-datalake] source dir not found: ${dir}`);
      process.exit(1);
    }
    // Required files must exist; optional may be skipped.
    for (const [filename, , , , , optional] of sources) {
      if (!optional && !existsSync(join(dir, filename))) {
        console.error(`[sync-datalake] source dir is missing required ${filename}: ${dir}`);
        process.exit(1);
      }
    }
    snapshotLabel = `local:${dir}`;
  } else {
    dir = mkdtempSync(join(tmpdir(), 'mcm-datalake-'));
    cleanupStaging = true;
    console.log(`[sync-datalake] staging ${dir}`);
    for (const [filename, , , , , optional] of sources) {
      try {
        fetchFromRclone(dir, filename);
      } catch (err) {
        if (optional) {
          console.warn(`[sync-datalake] optional ${filename} missing from lake — skipping`);
        } else {
          throw err;
        }
      }
    }
    snapshotLabel = RCLONE_REMOTE_DIR;
  }

  if (!existsSync(API_DATA_DIR)) mkdirSync(API_DATA_DIR, { recursive: true });

  for (const [filename, validator, exportName, jsFile, apiFile, optional] of sources) {
    const path = join(dir, filename);
    if (!existsSync(path)) {
      if (optional) continue;
      throw new Error(`Missing required CSV: ${filename}`);
    }
    const label = filename.replace('.csv', '');
    const result = ingestCsv(path, validator, label);
    if (args.dryRun) continue;
    const jsOut = writeGeneratedFile(jsFile, exportName, result.accepted, snapshotLabel);
    const apiOut = join(API_DATA_DIR, apiFile);
    writeFileSync(apiOut, JSON.stringify(result.accepted), 'utf8');
    console.log(`[sync-datalake] wrote ${jsOut}`);
    console.log(`[sync-datalake] wrote ${apiOut}`);
  }

  if (cleanupStaging) {
    // Leave staging dir behind for debugging — it's in /tmp and the OS will reap it.
  }
}

main();
