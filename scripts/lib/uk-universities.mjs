// Try to map a free-form provider string to a city + country.
// Falls back to null when no token in UK_UNIVERSITIES matches.
export function lookupUkUniversity(provider) {
  if (!provider) return null;
  const lower = provider.toLowerCase();
  // Sort keys longest-first so "leeds beckett" beats "leeds".
  const keys = Object.keys(UK_UNIVERSITIES).sort((a, b) => b.length - a.length);
  for (const k of keys) {
    if (lower.includes(k)) return UK_UNIVERSITIES[k];
  }
  return null;
}
